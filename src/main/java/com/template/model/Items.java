package com.template.model;

public class Items {
    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public Items setId(int id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public Items setName(String name) {
        this.name = name;
        return this;
    }
}
